    /**
     * Genera la cantidad "a" de clientes
     */
    private void GenerarClientes(int a){
        
        //Mantenemos los clientes lanzados en 250 hasta el final
        for(int i = 0; i<a; i++)
        {
            TareaCompradora cliente = new TareaCompradora(i, mercado);
            exe.submit(cliente);
            if(fin==true){ exe.shutdownNow();}
        }    
    }
    
