    private void FinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FinalizarActionPerformed

        //Por si toma alguna transición se repite con un tiempo de pausa
        // en las transiciones puede generar errores
        for (int i = 0; i < 3; i++) {
                exe.shutdownNow();
                mercado.finalizar();
                fin=true; 
                try{
                    sleep(150);
                }catch(InterruptedException e){}
        }
        mercado.limpiar();
        log.anadirACola("Se expulso a los clientes.");
        log.anadirACola("==\n==\n==\n==\n==\nSe forzo el final de la ejecucion");        

    }//GEN-LAST:event_FinalizarActionPerformed

